"""Exercise: Python List Comprehension"""
# Consider the following code:
# What will be the value of new_list?

fruits = ['apple', 'banana', 'cherry']
new_list = [x for x in fruits if x == 'banana']
print(new_list)  # ['banana']

# Fill in the missing parts to complete the list comprehension:

fruits = ["apple", "banana", "cherry"]
new_list = [x for x in fruits]
print(new_list)   # ['apple', 'banana', 'cherry']



